// Ejercicio 12: Operadores de asignación
let a = 5;
console.log('a =', a);
a += 3; // a = a + 3
console.log('a += 3 ->', a);
a *= 2; // a = a * 2
console.log('a *= 2 ->', a);
a -= 4; // a = a - 4
console.log('a -= 4 ->', a);
a /= 2; // a = a / 2
console.log('a /= 2 ->', a);

a = 10;
a %= 3; // a = a % 3
console.log('a %= 3 ->', a);
